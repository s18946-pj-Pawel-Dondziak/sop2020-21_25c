#!/bin/bash

echo -e $PATH # Przechowuje informacje na temat lokalizacji programów systemowych używanych w celu ich uruchomienia
echo -e $RANDOM # Zwraca losową liczbę
echo -e $PWD # Wyświetla pełną ścieżkę dostępu do katalogu w którym obecnie się znajdujemy
echo -e $PS1 # Zawiera sekwenkcje zwaną znakiem zachęty
echo -e $USER # Informacja o aktualnie zaklogowanym użykowniku
echo -e $HOSTNAME # Nazwa komputera
echo -e $OSTYPE # Informacje o typie systemu
